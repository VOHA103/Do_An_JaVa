    public void loadDuLieu() {
        ds = sachDAO.getDSSach();
        modelTB.setRowCount(0);
        for (Sach mt : ds) {
            Vector v = new Vector();
            v.add(mt.getMaSach());

            v.add(mt.getTenSach());
            v.add(mt.getTacGia());
            v.add(mt.getNamXuatBan());
            v.add(mt.getLoai());
            v.add(mt.getSoTrang());
            v.add(mt.getGia());
            v.add(mt.getTinhTrang());

            modelTB.addRow(v);
        }
        lb_soluong.setText(""+ds.size());
    }

    boolean ktraMaS(String maSach) {
        String ktra = "MS\\d+";
        if (maSach.matches(ktra)) {
            return true;
        }
        return false;
    }

