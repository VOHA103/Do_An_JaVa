    private void btn_inActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inActionPerformed
        String filef = "src//FILE//DS_Sach.txt";
        File file = new File(filef);

        try {
            if (JOptionPane.showConfirmDialog(this, "Ban co muon luu vao file txt khong", "Thong bao", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                FileWriter fw = new FileWriter(file);
                BufferedWriter bw = new BufferedWriter(fw);
                JOptionPane.showMessageDialog(this, "Ban da luu file thanh cong", "Thong bao", 2);

                for (int i = 0; i < tb_DSSach.getRowCount(); i++) {
                    for (int j = 0; j < tb_DSSach.getColumnCount(); j++) {

                        bw.write(tb_DSSach.getValueAt(i, j).toString() + "      ");

                    }

                    bw.newLine();
                }

                bw.close();
                fw.close();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btn_inActionPerformed

