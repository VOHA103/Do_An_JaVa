    private void btn_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_luuActionPerformed

        if (hoTRo.checkRong(txt_Gia) || hoTRo.checkRong(txt_loaiSach) || hoTRo.checkRong(txt_masach)
                || hoTRo.checkRong(txt_namXB) || hoTRo.checkRong(txt_soTrang) || hoTRo.checkRong(txt_tacgia)
                || hoTRo.checkRong(txt_tenSach) || hoTRo.checkRong(txt_tinhtrang)) {
            return;
        }
        if (!ktraMaS(txt_masach.getText())) {
            JOptionPane.showMessageDialog(this, "Ma sach co dang MSxx voi x la chu so", "Thong bao", 1);

            txt_masach.setText("");
            txt_masach.requestFocus();

            return;
        }
        if (!hoTRo.kt_soTrang(Integer.parseInt(txt_soTrang.getText()))) {
            JOptionPane.showMessageDialog(this, "Số trang sách phải lớn hơn 0", "Thong bao", 1);
            txt_soTrang.setText("");
            txt_soTrang.requestFocus();
            return;

        }
        if (!hoTRo.ktraNamXB(Integer.parseInt(txt_namXB.getText()))) {
            JOptionPane.showMessageDialog(this, "Nam XB tu 1975 den 2021", "Thong bao", 1);
            txt_namXB.setText("");
            txt_namXB.requestFocus();
            return;
        }
        if (!(txt_tinhtrang.getText().trim().equals("Còn") || txt_tinhtrang.getText().trim().equals("Hết"))) {
            JOptionPane.showMessageDialog(this, "Tình trạng Còn hoặc Hết", "Thong bao", 1);
            txt_tinhtrang.setText("");
            txt_tinhtrang.requestFocus();
            return;
        }
        if (JOptionPane.showConfirmDialog(this, "Ban co muon them khong", "Thong bao", 1) == JOptionPane.YES_NO_OPTION) {
            int i = sachDAO.themSach(txt_masach.getText().trim(), txt_tenSach.getText().trim(), txt_tacgia.getText().trim(),
                    Integer.parseInt(txt_namXB.getText()), txt_loaiSach.getText().trim(), Integer.parseInt(txt_soTrang.getText()),
                    Integer.parseInt(txt_Gia.getText()), txt_tinhtrang.getText().trim());

            if (i != -1) {
                JOptionPane.showMessageDialog(this, "Ban da them thanh cong", "Thong bao", 1);

                loadDuLieu();
                reSet();
            }
        }


    }//GEN-LAST:event_btn_luuActionPerformed

