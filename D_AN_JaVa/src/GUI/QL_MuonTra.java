    private void btn_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_themActionPerformed

        if (hoTRo.checkRong(txt_mamuontra) || hoTRo.checkRong(txt_masv) || hoTRo.checkRong(txt_masach) || hoTRo.checkRong(txt_hinhthucmuon) || hoTRo.checkRong(txt_ngaymuon) || hoTRo.checkRong(txt_ngaytra) || hoTRo.checkRong(txt_quanlythuvien)) {
            return;
        }

        if (!ktraMaMT(txt_mamuontra.getText())) {
            JOptionPane.showMessageDialog(this, "Ma MT co dang MTxx voi x la chu so", "Thong bao", 1);

            txt_mamuontra.setText("");
            txt_mamuontra.requestFocus();

            return;
        }

        if (!ktraMaSV(txt_masv.getText())) {
            JOptionPane.showMessageDialog(this, "Ma SV co dang SVxx voi x la chu so", "Thong bao", 1);

            txt_masv.setText("");
            txt_masv.requestFocus();

            return;
        }
        if (!ktraMaS(txt_masach.getText())) {
            JOptionPane.showMessageDialog(this, "Ma Sach co dang MSxx voi x la chu so", "Thong bao", 1);

            txt_masach.setText("");
            txt_masach.requestFocus();

            return;
        }
        if (!(txt_hinhthucmuon.getText().trim().equals("Mượn tại chỗ") || txt_hinhthucmuon.getText().trim().equals("Mượn về nhà"))) {
            JOptionPane.showMessageDialog(this, " Hinh thuc muon Mượn tại chỗ hoạc Mượn về nhà)", "Thong bao", 1);
            txt_hinhthucmuon.setText("");
            txt_hinhthucmuon.requestFocus();
            return;
        }
        if (Integer.parseInt(txt_sluongmuon.getText().toString()) > 3) {
            JOptionPane.showMessageDialog(null, "Số lượng mượn không được lớn hơn 3 quyển", "thong bao", 1);
            txt_sluongmuon.setText("");
            txt_sluongmuon.requestFocus();
            return;
        }
        Date ngayMuon, ngayTra;
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        try {
            ngayMuon = df.parse(txt_ngaymuon.getText().trim());
            ngayTra = df.parse(txt_ngaytra.getText().trim());
        } catch (Exception e) {
            System.out.println("");
            return;
        }
        System.out.println(ngayMuon + " " + ngayTra);
        if (ngayMuon.compareTo(ngayTra) >= 0) {
            JOptionPane.showMessageDialog(null, "Ngay muon luon nho hon hoac bang ngay tra", "thong bao", 1);
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Ban co muon cho muon sach   nay khong", "Thong bao", 1) == JOptionPane.YES_NO_OPTION) {
            int i = muonTraSachDAO.them(txt_mamuontra.getText().trim(), txt_masv.getText().trim(), txt_masach.getText().trim(),   Integer.parseInt(txt_sluongmuon.getText()), txt_hinhthucmuon.getText().trim(), txt_quanlythuvien.getText().trim(),
                  txt_ngaymuon.getText().trim(), txt_ngaytra.getText().trim());

            if (i != -1) {
                JOptionPane.showMessageDialog(this, "Ban da them thanh cong", "Thong bao", 1);
                reset();
                loadDuLieu();

            }
        }

    }//GEN-LAST:event_btn_themActionPerformed

