    public void loadDuLieu() {
        ds = muonTraSachDAO.getDSMuonTRa();
        modelTB.setRowCount(0);
        for (MuonTRaSach mt : ds) {
            Vector v = new Vector();
            v.add(mt.getMaMuonTRa());

            v.add(mt.getMaSinhVien());
            v.add(mt.getMaSach());
            v.add(mt.getSoLuongMuon());
            v.add(mt.getHinhThucMuon());
            v.add(mt.getQuanLyThuVien());
            v.add(mt.getNgayMuon());
            v.add(mt.getNgayTRa());
            modelTB.addRow(v);
        }
    }

